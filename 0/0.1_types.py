"""
Programming languages have a bunch of "types", which represent different things, some examples:

* Integers - int - whole numbers, e.g. 0, 10, -123
* Floats - float (sometimes double) - decimals, e.g. 0.5, -10.8
* strings - str - text, e.g. "Hello"
* booleans - bool - True/False
* tuples - A fixed size collection of things e.g. (1, 2)
* lists/arrays - a list of things, e.g. [1, 2, 3, 4, 5]
* sets - like lists, but without any duplicates
* dictionary/map - a mapping from one thing to another, e.g. {"blah": 50, "meh": 172}
* Null/None - indicates the lack of anything
* enums - these basically define a constant set of values that a variable can take e.g.
    - a variable to represent the state of a video could be allowed to be: PLAYING, PAUSED, STOPPED, but nothing else

You can assign instances of these types to variables to save them, and then you can manipulate them
"""

